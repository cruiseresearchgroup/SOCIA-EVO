def set_global_seed(seed: int) -> None:
    random.seed(seed)
    try:
        import numpy as np  # type: ignore
        np.random.seed(seed)
    except Exception:
        pass


def ensure_dir(path: str) -> None:
    d = os.path.dirname(os.path.abspath(path))
    if d and not os.path.exists(d):
        os.makedirs(d, exist_ok=True)


def write_json(path: str, obj: Any) -> None:
    ensure_dir(path)
    with open(path, "w", encoding="utf-8") as f:
        json.dump(obj, f, indent=2, ensure_ascii=False)


def write_jsonl(path: str, records: Iterable[Dict[str, Any]]) -> None:
    ensure_dir(path)
    with open(path, "w", encoding="utf-8") as f:
        for r in records:
            f.write(json.dumps(r, ensure_ascii=False) + "\n")


def tokenize(text: str) -> List[str]:
    tokens = re.findall(r"[a-zA-Z']+", text.lower())
    return tokens


POS_WORDS = set("""
amazing awesome great excellent fantastic love loved good nice wonderful perfect thrilled satisfied delighted sturdy durable comfortable sleek elegant efficient fast smooth reliable tasty flavorful fresh crisp juicy convenient helpful friendly polite responsive
""".strip().split())
NEG_WORDS = set("""
bad terrible awful horrible disappointed disappointing poor slow broken flimsy cheap hate hated worst dirty noisy rude unhelpful late leaking damaged unusable confusing buggy crash crashes laggy bitter stale bland soggy
""".strip().split())


def sentiment_score(text: str) -> float:
    """
    Simple polarity score in [0,1] computed from token counts.
    """
    toks = tokenize(text)
    if not toks:
        return 0.5
    pos = sum(1 for t in toks if t in POS_WORDS)
    neg = sum(1 for t in toks if t in NEG_WORDS)
    total = pos + neg
    if total == 0:
        return 0.5
    score = (pos - neg) / max(1, total)
    # map [-1,1] to [0,1]
    return 0.5 * (score + 1.0)


def jaccard_similarity(a: str, b: str) -> float:
    sa, sb = set(tokenize(a)), set(tokenize(b))
    if not sa and not sb:
        return 1.0
    if not sa or not sb:
        return 0.0
    inter = len(sa & sb)
    union = len(sa | sb)
    return inter / max(1, union)


def sentence_count(text: str) -> int:
    sents = re.split(r"[.!?]+", text.strip())
    return max(0, len([s for s in sents if s.strip()]))


