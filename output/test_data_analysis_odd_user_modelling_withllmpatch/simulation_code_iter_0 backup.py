def get_openai_api_key():
    api_key = os.environ.get("OPENAI_API_KEY")
    if api_key:
        return api_key
    raise ValueError("OpenAI API key not found in environment")


def call_gpt5_with_responses_api(prompt: str, model: str = "gpt-5", max_output_tokens: int = 4000) -> str:
    """
    Invoke OpenAI Responses API to get text output for the given prompt.
    If API or network fails, falls back to a deterministic templated output.
    """
    try:
        api_key = get_openai_api_key()
        if OpenAI is None:
            raise RuntimeError("openai package not installed")
        client = OpenAI(api_key=api_key)

        responses_kwargs = {
            "model": model,
            "input": [
                {"role": "user", "content": [{"type": "input_text", "text": prompt}]}
            ],
            "max_output_tokens": max_output_tokens,
        }

        resp = client.responses.create(**responses_kwargs)

        def extract_response(resp_obj):
            if hasattr(resp_obj, "output_text") and isinstance(resp_obj.output_text, str):
                return resp_obj.output_text
            try:
                output = getattr(resp_obj, "output", None)
                if output and isinstance(output, list):
                    content = output[0].get("content") if isinstance(output[0], dict) else None
                    if content and isinstance(content, list) and len(content) > 0:
                        text = content[0].get("text")
                        if isinstance(text, str):
                            return text
            except Exception:
                pass
            return str(resp_obj)

        return extract_response(resp)
    except Exception:
        # Fallback deterministic response to keep pipeline running offline
        return (
            "This is a synthesized review generated by a local fallback because the LLM "
            "service is unavailable. The content adheres to the plan and platform policy. "
            "Pros: specific aspects addressed; Cons: none major observed. Overall, a solid experience."
        )


