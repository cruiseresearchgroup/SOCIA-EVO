# QA Consistency
# ------------------------------

@dataclass
class QAParams:
    consistency_threshold: float
    max_auto_fix_attempts: int
    penalty_weight_style_violations: float


@dataclass
class QAResult:
    text: str
    stars: int
    consistency_score: float
    aspect_coverage: float
    length_deviation: float


class QAConsistency:
    def __init__(self, platform_policy: Dict[str, Any], params: QAParams):
        self.platform_policy = platform_policy
        self.params = params
        self.consistency_score: float = 1.0
        self.auto_fix_attempts: int = 0

    def evaluate(self, text: str, stars: int, plan: Plan) -> Tuple[float, float, float]:
        # Sentiment vs rating agreement
        pol = sentiment_score(text)  # [0,1]
        target_bucket = (stars - 1) / 4.0  # 0..1
        sent_agree = 1.0 - min(1.0, abs(pol - target_bucket) / 0.5)  # 1 when close; 0 when far

        # Aspect coverage: proportion of planned aspects mentioned
        toks = set(tokenize(text))
        covered = sum(1 for a in plan.planned_aspects if a in toks)
        aspect_cov = covered / max(1, len(plan.planned_aspects))

        # Length deviation
        sc = sentence_count(text)
        len_dev = abs(sc - plan.length_target)
        return sent_agree, aspect_cov, float(len_dev)

    def check_and_fix(
        self,
        text: str,
        stars: int,
        plan: Plan,
        author: ReviewAuthor,
        persona: PersonaProfile,
        item_ctx: ItemProfile,
    ) -> QAResult:
        self.auto_fix_attempts = 0
        sent_agree, aspect_cov, len_dev = self.evaluate(text, stars, plan)
        style_penalty = 0.0
        # simple style constraints
        if sentence_count(text) < self.platform_policy.get("min_sentences", 2):
            style_penalty += self.params.penalty_weight_style_violations
        if sentence_count(text) > self.platform_policy.get("max_sentences", 6):
            style_penalty += self.params.penalty_weight_style_violations
        consistency = max(0.0, min(1.0, 0.6 * sent_agree + 0.3 * aspect_cov + 0.1 * (1.0 - min(1.0, len_dev / 3.0)) - style_penalty))

        while consistency < self.params.consistency_threshold and self.auto_fix_attempts < self.params.max_auto_fix_attempts:
            self.auto_fix_attempts += 1
            # Nudge tone in prompt based on discrepancy
            pol = sentiment_score(text)
            target_bucket = (stars - 1) / 4.0
            if pol < target_bucket:
                plan.tone_targets = "more positive"
            else:
                plan.tone_targets = "more critical"
            # Regenerate
            text = author.generate(plan, persona, item_ctx, self.platform_policy, llm_temperature=0.4, style_alignment_weight=author.policy.style_alignment_weight, max_tokens=256)
            sent_agree, aspect_cov, len_dev = self.evaluate(text, stars, plan)
            consistency = max(0.0, min(1.0, 0.6 * sent_agree + 0.3 * aspect_cov + 0.1 * (1.0 - min(1.0, len_dev / 3.0)) - style_penalty))

        return QAResult(text=text, stars=stars, consistency_score=consistency, aspect_coverage=aspect_cov, length_deviation=len_dev)


