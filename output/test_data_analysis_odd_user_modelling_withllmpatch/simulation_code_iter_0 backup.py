# Star Rater
# ------------------------------

@dataclass
class RatingParams:
    mapping_slope: float
    mapping_intercept: float
    user_bias_weight: float
    item_bias_weight: float
    uncertainty_scale: float


class StarRater:
    def __init__(self, indexer: DataIndexer, params: RatingParams):
        self.indexer = indexer
        self.params = params

    def rate(self, text: str, user_id: str, item_id: str) -> int:
        s = sentiment_score(text)  # [0,1]
        # logistic mapping from sentiment to [1,5]
        def sigmoid(x: float) -> float:
            return 1.0 / (1.0 + math.exp(-x))
        user_prior = self.indexer.get_user_prior(user_id) - 3.0
        item_prior = self.indexer.get_item_prior(item_id) - 3.0
        z = self.params.mapping_slope * (s - 0.5) + self.params.mapping_intercept
        z += self.params.user_bias_weight * user_prior + self.params.item_bias_weight * item_prior
        expected = 1.0 + 4.0 * sigmoid(z)
        noise = random.gauss(0.0, self.params.uncertainty_scale * 0.2)
        val = expected + noise
        stars = int(round(min(5.0, max(1.0, val))))
        return stars


