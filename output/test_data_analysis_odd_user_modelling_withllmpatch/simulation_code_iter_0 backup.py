@dataclass
class SimParams:
    neighbor_weight: float
    ctx_merge_weight: float
    aspect_topk: int
    length_target_mean: int
    plan_diversity_temp: float
    llm_temperature: float
    style_alignment_weight: float
    mapping_slope: float
    mapping_intercept: float
    user_bias_weight: float
    item_bias_weight: float
    uncertainty_scale: float
    consistency_threshold: float
    max_auto_fix_attempts: int
    objective_weights: Dict[str, float]


class Evaluator:
    def __init__(self):
        self.metrics: Dict[str, float] = {}
        self.by_segment: Dict[str, Dict[str, float]] = {}

    @staticmethod
    def compute_metrics(records: List[Dict[str, Any]]) -> Dict[str, float]:
        # records contain: gt_stars, gen_stars, gt_text, gen_text, qa fields
        star_pairs = [(r.get("gt_stars"), r.get("gen_stars")) for r in records if r.get("gt_stars") is not None and r.get("gen_stars") is not None]
        mae = sum(abs(a - b) for a, b in star_pairs) / max(1, len(star_pairs)) if star_pairs else 0.0
        rmse = math.sqrt(sum((a - b) ** 2 for a, b in star_pairs) / max(1, len(star_pairs))) if star_pairs else 0.0
        sim_vals = [jaccard_similarity(r.get("gt_text", "") or "", r.get("gen_text", "") or "") for r in records]
        text_sim = sum(sim_vals) / max(1, len(sim_vals)) if sim_vals else 0.0
        sent_agree_vals = []
        for r in records:
            pol = sentiment_score(r.get("gen_text", "") or "")
            bucket = ((r.get("gen_stars", 3)) - 1) / 4.0
            sent_agree_vals.append(1.0 - min(1.0, abs(pol - bucket) / 0.5))
        sent_agree = sum(sent_agree_vals) / max(1, len(sent_agree_vals)) if sent_agree_vals else 0.0
        aspect_cov_vals = [r.get("qa", {}).get("aspect_coverage", 0.0) for r in records]
        aspect_cov = sum(aspect_cov_vals) / max(1, len(aspect_cov_vals)) if aspect_cov_vals else 0.0
        consistency_vals = [r.get("qa", {}).get("consistency_score", 0.0) for r in records]
        consistency = sum(consistency_vals) / max(1, len(consistency_vals)) if consistency_vals else 0.0
        len_dev_vals = [r.get("qa", {}).get("length_deviation", 0.0) for r in records]
        len_dev = sum(len_dev_vals) / max(1, len(len_dev_vals)) if len_dev_vals else 0.0
        return {
            "RMSE_stars": rmse,
            "MAE_stars": mae,
            "Text_Similarity": text_sim,
            "Sentiment_Agreement": sent_agree,
            "Aspect_Coverage": aspect_cov,
            "Consistency_Score": consistency,
            "Length_Deviation": len_dev,
        }

    def evaluate(self, records: List[Dict[str, Any]]) -> Dict[str, float]:
        self.metrics = self.compute_metrics(records)
        return self.metrics


class ParameterTuner:
    def __init__(self, num_trials: int = 5, early_stop_patience: int = 3):
        self.num_trials = num_trials
        self.early_stop_patience = early_stop_patience
        self.best_params: Optional[SimParams] = None
        self.best_objective: float = float("inf")
        self.history: List[Tuple[SimParams, float]] = []

    @staticmethod
    def sample_params() -> SimParams:
        def ru(a: float, b: float) -> float:
            return random.uniform(a, b)
        def ri(a: int, b: int) -> int:
            return random.randint(a, b)
        # objective weights sampling and normalization
        w_stars = ru(0.4, 0.8)
        w_text = ru(0.2, 0.6)
        w_cons = ru(0.0, 0.3)
        s = w_stars + w_text + w_cons
        ow = {"stars": w_stars / s, "text": w_text / s, "consistency": w_cons / s}
        return SimParams(
            neighbor_weight=ru(0.0, 0.5),
            ctx_merge_weight=ru(0.2, 0.8),
            aspect_topk=ri(3, 6),
            length_target_mean=ri(2, 6),
            plan_diversity_temp=ru(0.3, 1.2),
            llm_temperature=ru(0.2, 0.9),
            style_alignment_weight=ru(0.3, 1.0),
            mapping_slope=ru(2.0, 8.0),
            mapping_intercept=ru(-2.0, 2.0),
            user_bias_weight=ru(0.0, 1.0),
            item_bias_weight=ru(0.0, 1.0),
            uncertainty_scale=ru(0.1, 1.0),
            consistency_threshold=ru(0.6, 0.9),
            max_auto_fix_attempts=ri(0, 2),
            objective_weights=ow,
        )

    def objective_for_trial(
        self,
        params: SimParams,
        indexer: DataIndexer,
        train_indices: List[int],
    ) -> float:
        # Run a fast simulation pass on training set and compute weighted loss
        author = ReviewAuthor(AuthorPolicy(style_alignment_weight=params.style_alignment_weight, max_revision_loops=params.max_auto_fix_attempts))
        persona_prof = PersonaProfiler(indexer)
        item_prof = ItemProfiler(indexer)
        rater = StarRater(indexer, RatingParams(
            mapping_slope=params.mapping_slope,
            mapping_intercept=params.mapping_intercept,
            user_bias_weight=params.user_bias_weight,
            item_bias_weight=params.item_bias_weight,
            uncertainty_scale=params.uncertainty_scale,
        ))
        qa = QAConsistency(indexer.platform_policy, QAParams(
            consistency_threshold=params.consistency_threshold,
            max_auto_fix_attempts=params.max_auto_fix_attempts,
            penalty_weight_style_violations=0.05,
        ))

        total_star_err = 0.0
        total_text_loss = 0.0
        total_cons_loss = 0.0
        count = 0
        # Limit to avoid excessive API calls during tuning
        max_train_eval = min(50, len(train_indices))
        sample_indices = random.sample(train_indices, max_train_eval) if len(train_indices) > max_train_eval else train_indices
        for idx in sample_indices:
            rec = indexer.records[idx]
            persona = persona_prof.build(rec.user_id, neighbor_weight=params.neighbor_weight)
            item = item_prof.build(rec.item_id)
            plan = PlanComposer().compose(
                persona, item, indexer.platform_policy,
                aspect_topk=params.aspect_topk,
                ctx_merge_weight=params.ctx_merge_weight,
                length_target_mean=params.length_target_mean,
                plan_diversity_temp=params.plan_diversity_temp,
            )
            gen_text = ReviewAuthor(author.policy).generate(
                plan, persona, item, indexer.platform_policy,
                llm_temperature=params.llm_temperature,
                style_alignment_weight=params.style_alignment_weight,
                max_tokens=256,
            )
            pred_stars = rater.rate(gen_text, rec.user_id, rec.item_id)
            qa_res = qa.check_and_fix(gen_text, pred_stars, plan, author, persona, item)

            # Loss components
            if rec.stars is not None:
                total_star_err += abs(rec.stars - qa_res.stars)
            ref_text = rec.review or ""
            sim = jaccard_similarity(ref_text, qa_res.text)
            total_text_loss += (1.0 - sim)
            total_cons_loss += (1.0 - qa_res.consistency_score)
            count += 1

        if count == 0:
            return float("inf")
        w = params.objective_weights
        objective = w["stars"] * (total_star_err / count) + w["text"] * (total_text_loss / count) + w["consistency"] * (total_cons_loss / count)
        return objective

    def tune(self, indexer: DataIndexer) -> SimParams:
        train_indices = indexer.split_index.get("train", list(range(len(indexer.records))))
        patience = 0
        for t in range(self.num_trials):
            params = self.sample_params()
            obj = self.objective_for_trial(params, indexer, train_indices)
            self.history.append((params, obj))
            if obj < self.best_objective:
                self.best_objective = obj
                self.best_params = params
                patience = 0
            else:
                patience += 1
            if patience >= self.early_stop_patience:
                break
        assert self.best_params is not None
        return self.best_params


