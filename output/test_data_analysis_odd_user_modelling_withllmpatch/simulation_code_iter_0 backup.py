@dataclass
class AuthorPolicy:
    generation_guidelines: str = "Platform-safe, concise, specific, avoid profanity."
    style_alignment_weight: float = 0.7
    temperature_schedule: float = 0.5
    max_revision_loops: int = 1


class ReviewAuthor:
    def __init__(self, policy: Optional[AuthorPolicy] = None):
        self.policy = policy or AuthorPolicy()
        self.last_generated_text_quality_score: float = 0.0
        self.revision_count: int = 0

    def _build_prompt(self, plan: Plan, persona: PersonaProfile, item_ctx: ItemProfile, platform_policy: Dict[str, Any]) -> str:
        aspects_str = ", ".join(plan.planned_aspects) if plan.planned_aspects else "general impressions"
        prompt = (
            f"You are writing a product review.\n"
            f"Platform policy: {platform_policy.get('style')}. "
            f"Target length: {plan.length_target} sentences. Tone: {plan.tone_targets}.\n"
            f"Persona: user_id={persona.user_id}; baseline_leniency={persona.baseline_leniency:.2f}; "
            f"style={persona.tone_style_prior}; domain_familiarity={persona.domain_familiarity:.2f}.\n"
            f"Item: item_id={item_ctx.item_id}; prior_quality={item_ctx.quality_prior:.2f}; "
            f"domain_tags={', '.join(item_ctx.domain_tags) if item_ctx.domain_tags else 'general'}.\n"
            f"Plan:\n- Cover aspects: {aspects_str}\n- Be {plan.tone_targets} and specific with examples.\n"
            f"Guidelines: {self.policy.generation_guidelines}\n"
            f"Write the review now without including headings or bullet points."
        )
        return prompt

    def generate(
        self,
        plan: Plan,
        persona: PersonaProfile,
        item_ctx: ItemProfile,
        platform_policy: Dict[str, Any],
        llm_temperature: float,
        style_alignment_weight: float,
        model: str = "gpt-5",
        max_tokens: int = 512,
    ) -> str:
        # Build composite prompt and invoke the LLM
        prompt = self._build_prompt(plan, persona, item_ctx, platform_policy)
        # Encode additional hints in the prompt since we cannot directly set temperature via Responses API here
        prompt += f"\nWhen choosing wording, align {int(100*style_alignment_weight)}% to persona style. Avoid generic fluff."
        text = call_gpt5_with_responses_api(prompt=prompt, model=model, max_output_tokens=max_tokens)
        # Simple post-processing: clamp sentence count to target if drastically off (truncate)
        sents = re.split(r"(?<=[.!?])\s+", text.strip())
        if len(sents) > max(2 * plan.length_target, plan.length_target + 3):
            text = " ".join(sents[:max(1, plan.length_target + 1)])
        return text


