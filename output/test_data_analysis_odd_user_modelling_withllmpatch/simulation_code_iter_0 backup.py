# Persona Profiler
# ------------------------------

@dataclass
class PersonaProfile:
    user_id: str
    baseline_leniency: float
    verbosity_prior: float
    tone_style_prior: str
    domain_familiarity: float
    leniency_drift: float
    aspect_preference_weights: Dict[str, float]
    style_vector: Dict[str, float]
    recent_sentiment_bias: float


class PersonaProfiler:
    def __init__(self, indexer: DataIndexer):
        self.indexer = indexer

    def build(self, user_id: str, neighbor_weight: float = 0.0) -> PersonaProfile:
        texts = self.indexer.get_user_history_texts(user_id)
        baseline = self.indexer.get_user_prior(user_id)
        # aspect preferences by counting mentions
        aspect_weights: Dict[str, float] = {a: 0.0 for a in self.indexer.aspect_vocab}
        for t in texts:
            toks = tokenize(t)
            for a in self.indexer.aspect_vocab:
                if a in toks:
                    aspect_weights[a] += 1.0
        # normalize
        total = sum(aspect_weights.values()) or 1.0
        for a in aspect_weights:
            aspect_weights[a] /= total

        # neighbor influence (no social graph -> approximate with global average user prior)
        if neighbor_weight > 0.0:
            neighbor_leniency = self.indexer.global_mean
            baseline = (1 - neighbor_weight) * baseline + neighbor_weight * neighbor_leniency

        verbosity_prior = min(1.0, 0.5 + 0.1 * len(texts))
        style = "formal" if baseline <= 3.0 else "enthusiastic"
        style_vector = {"formal": 1.0 if style == "formal" else 0.2, "enthusiastic": 1.0 if style == "enthusiastic" else 0.2}
        recent_sent_bias = 0.0
        return PersonaProfile(
            user_id=user_id,
            baseline_leniency=baseline,
            verbosity_prior=verbosity_prior,
            tone_style_prior=style,
            domain_familiarity=min(1.0, 0.1 * len(texts)),
            leniency_drift=0.0,
            aspect_preference_weights=aspect_weights,
            style_vector=style_vector,
            recent_sentiment_bias=recent_sent_bias,
        )


