@dataclass
class ItemProfile:
    item_id: str
    quality_prior: float
    aspect_summary: Dict[str, float]
    domain_tags: List[str]
    freshness_score: float
    controversy: float
    aspect_confidence: float


class ItemProfiler:
    def __init__(self, indexer: DataIndexer, reputation_inertia: float = 0.5):
        self.indexer = indexer
        self.reputation_inertia = reputation_inertia

    def build(self, item_id: str) -> ItemProfile:
        texts = self.indexer.get_item_history_texts(item_id)
        prior = self.indexer.get_item_prior(item_id)
        # aspect summary
        aspect_summary: Dict[str, float] = {a: 0.0 for a in self.indexer.aspect_vocab}
        sentiments: List[float] = []
        for t in texts:
            s = sentiment_score(t)
            sentiments.append(s)
            toks = tokenize(t)
            for a in self.indexer.aspect_vocab:
                if a in toks:
                    aspect_summary[a] += 1.0
        # normalize to probabilities
        total = sum(aspect_summary.values()) or 1.0
        for a in aspect_summary:
            aspect_summary[a] /= total

        # controversy as variance of stars for item if available
        star_vals = [self.indexer.records[i].stars for i in self.indexer.item_index.get(item_id, []) if self.indexer.records[i].stars is not None]
        if star_vals:
            mean = sum(star_vals) / len(star_vals)
            var = sum((x - mean) ** 2 for x in star_vals) / len(star_vals)
        else:
            var = 0.0

        domain_tags = [a for a, w in aspect_summary.items() if w > 0.1][:3]
        aspect_conf = min(1.0, 0.1 * len(texts))
        freshness = min(1.0, 0.05 * len(texts))
        return ItemProfile(
            item_id=item_id,
            quality_prior=prior * (1 - self.reputation_inertia) + self.indexer.get_item_prior(item_id) * self.reputation_inertia,
            aspect_summary=aspect_summary,
            domain_tags=domain_tags,
            freshness_score=freshness,
            controversy=var,
            aspect_confidence=aspect_conf,
        )


# ------------------------------
# Plan Composer
# ------------------------------

@dataclass
class Plan:
    planned_aspects: List[str]
    tone_targets: str
    length_target: int


class PlanComposer:
    def __init__(self, aspect_selection_rules: Optional[Dict[str, Any]] = None):
        self.rules = aspect_selection_rules or {}

    def compose(
        self,
        persona: PersonaProfile,
        item: ItemProfile,
        platform_policy: Dict[str, Any],
        aspect_topk: int,
        ctx_merge_weight: float,
        length_target_mean: int,
        plan_diversity_temp: float,
    ) -> Plan:
        # blend persona and item aspect distributions
        blended: Dict[str, float] = {}
        for a in item.aspect_summary.keys():
            pv = persona.aspect_preference_weights.get(a, 0.0)
            iv = item.aspect_summary.get(a, 0.0)
            blended[a] = ctx_merge_weight * pv + (1 - ctx_merge_weight) * iv

        # sample top-k aspects with diversity via temperature
        def softmax(d: Dict[str, float], temp: float) -> Dict[str, float]:
            m = max(d.values()) if d else 0.0
            exps = {k: math.exp((v - m) / max(1e-6, temp)) for k, v in d.items()}
            s = sum(exps.values()) or 1.0
            return {k: v / s for k, v in exps.items()}

        probs = softmax(blended, max(1e-6, plan_diversity_temp))
        aspects_sorted = sorted(probs.items(), key=lambda kv: kv[1], reverse=True)
        planned = [a for a, _ in aspects_sorted[:max(1, aspect_topk)]]

        # tone target based on persona leniency and item prior
        expected = 0.5 * (persona.baseline_leniency / 5.0) + 0.5 * (item.quality_prior / 5.0)
        tone = "positive" if expected >= 0.6 else "neutral" if expected >= 0.45 else "negative"

        # length target
        min_s = platform_policy.get("min_sentences", 2)
        max_s = platform_policy.get("max_sentences", 6)
        lt = int(round(length_target_mean))
        lt = min(max(lt, min_s), max_s)
        return Plan(planned_aspects=planned, tone_targets=tone, length_target=lt)


