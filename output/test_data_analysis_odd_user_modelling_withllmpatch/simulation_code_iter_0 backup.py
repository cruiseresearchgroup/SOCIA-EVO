@dataclass
class DataIndexer:
    records: List[InteractionRecord]
    user_index: Dict[str, List[int]] = field(default_factory=dict)
    item_index: Dict[str, List[int]] = field(default_factory=dict)
    split_index: Dict[str, List[int]] = field(default_factory=dict)
    aspect_vocab: List[str] = field(default_factory=list)
    platform_policy: Dict[str, Any] = field(default_factory=dict)
    user_priors: Dict[str, float] = field(default_factory=dict)
    item_priors: Dict[str, float] = field(default_factory=dict)
    global_mean: float = 3.5

    @staticmethod
    def load_from_file_or_synthetic(path: str = "interactions.jsonl", seed: int = 7) -> "DataIndexer":
        set_global_seed(seed)
        records: List[InteractionRecord] = []
        if os.path.exists(path):
            try:
                with open(path, "r", encoding="utf-8") as f:
                    for line in f:
                        if not line.strip():
                            continue
                        d = json.loads(line)
                        rec = InteractionRecord(
                            user_id=str(d.get("user_id")),
                            item_id=str(d.get("item_id")),
                            stars=int(d["stars"]) if "stars" in d and d["stars"] is not None else None,
                            review=d.get("review"),
                            datatype=d.get("datatype"),
                            timestamp=float(d["timestamp"]) if "timestamp" in d and d["timestamp"] is not None else None,
                        )
                        records.append(rec)
            except Exception as e:
                print(f"Error reading {path}: {e}", file=sys.stderr)
                records = []
        if not records:
            # synthetic minimal dataset
            synth = [
                {"user_id": "u1", "item_id": "i1", "stars": 5, "review": "Amazing quality and fast delivery. Very satisfied!", "datatype": "train"},
                {"user_id": "u1", "item_id": "i2", "stars": 4, "review": "Good value and decent performance.", "datatype": "train"},
                {"user_id": "u2", "item_id": "i1", "stars": 3, "review": "Okay overall, design could be better.", "datatype": "train"},
                {"user_id": "u2", "item_id": "i3", "stars": 2, "review": "Slow and somewhat disappointing build.", "datatype": "test"},
                {"user_id": "u3", "item_id": "i2", "stars": 1, "review": "Terrible experience, broken on arrival and unhelpful support.", "datatype": "test"},
            ]
            records = [InteractionRecord(**d) for d in synth]
        di = DataIndexer(records=records)
        di._build_indices()
        di._compute_priors()
        di.aspect_vocab = [
            "quality", "price", "value", "design", "durability", "performance", "delivery", "packaging",
            "usability", "features", "battery", "size", "comfort", "customer_service"
        ]
        di.platform_policy = {
            "style": "concise, specific, respectful, no profanity",
            "min_sentences": 2,
            "max_sentences": 6,
            "profanity_list": [],
        }
        return di

    def _build_indices(self) -> None:
        self.user_index.clear()
        self.item_index.clear()
        self.split_index.clear()
        for idx, r in enumerate(self.records):
            self.user_index.setdefault(r.user_id, []).append(idx)
            self.item_index.setdefault(r.item_id, []).append(idx)
            split = r.datatype if r.datatype in ("train", "test") else None
            if split:
                self.split_index.setdefault(split, []).append(idx)
        # if no split labels, do random split 80/20
        if not self.split_index:
            indices = list(range(len(self.records)))
            random.shuffle(indices)
            cut = int(0.8 * len(indices))
            self.split_index["train"] = indices[:cut]
            self.split_index["test"] = indices[cut:]

    def _compute_priors(self) -> None:
        # Compute user/item priors from available labeled data
        user_s: Dict[str, List[int]] = {}
        item_s: Dict[str, List[int]] = {}
        stars_all: List[int] = []
        for r in self.records:
            if r.stars is not None:
                user_s.setdefault(r.user_id, []).append(r.stars)
                item_s.setdefault(r.item_id, []).append(r.stars)
                stars_all.append(r.stars)
        self.global_mean = sum(stars_all) / len(stars_all) if stars_all else 3.5
        self.user_priors = {u: (sum(v) / len(v)) for u, v in user_s.items()}
        self.item_priors = {i: (sum(v) / len(v)) for i, v in item_s.items()}

    def get_user_history_texts(self, user_id: str) -> List[str]:
        return [self.records[i].review or "" for i in self.user_index.get(user_id, []) if self.records[i].review]

    def get_item_history_texts(self, item_id: str) -> List[str]:
        return [self.records[i].review or "" for i in self.item_index.get(item_id, []) if self.records[i].review]

    def get_user_prior(self, user_id: str) -> float:
        return self.user_priors.get(user_id, self.global_mean)

    def get_item_prior(self, item_id: str) -> float:
        return self.item_priors.get(item_id, self.global_mean)


